const firstnumber = 3;
const secondnumber = 5;
let operator = "/";

operator === "+"
  ? console.log(firstnumber + secondnumber)
  : operator === "-"
  ? console.log(firstnumber - secondnumber)
  : operator === "*"
  ? console.log(firstnumber * secondnumber)
  : operator === "/"
  ? console.log(firstnumber / secondnumber)
  : console.log();
