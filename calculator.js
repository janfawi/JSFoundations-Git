const firstnumber = 3;
const secondnumber = 5;
let operator = "/";
if (operator === "+") {
  console.log(firstnumber + secondnumber);
}
if (operator === "-") {
  console.log(firstnumber - secondnumber);
}
if (operator === "*") {
  console.log(firstnumber * secondnumber);
}
if (operator === "/") {
  console.log(firstnumber / secondnumber);
}
